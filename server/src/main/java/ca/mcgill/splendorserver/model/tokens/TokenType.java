package ca.mcgill.splendorserver.model.tokens;

/**
 * Type of Token.
 * Each type corresponds to its own color.
 */
public enum TokenType {

  /**
   * Diamond token type.
   */
  DIAMOND,
  /**
   * Sapphire token type.
   */
  SAPPHIRE,
  /**
   * Emerald token type.
   */
  EMERALD,
  /**
   * Ruby token type.
   */
  RUBY,
  /**
   * Onyx token type.
   */
  ONYX,
  /**
   * Gold token type.
   */
  GOLD
}
/*
* Types base1, 2 and 3 have one of each token per variant
*/